#!/usr/bin/env -S bun run
// scripts/emit-version-modules.ts
//
// Codegen for the per-version TypeScript wrappers that sit alongside
// each `generated/<version>/*.json` defs.
//
// For every version we track, we emit one file:
//
//   generated/<version>/index.ts      one module, all exports —
//                                     createParser / createTokenizer
//                                     pre-bound to this version's
//                                     dumps, plus a `parse(sql)` /
//                                     `tokenize(sql)` convenience
//                                     API, re-exported types, and the
//                                     raw defs objects.
//
// The reason this file exists at all (vs. hand-authored per-version
// modules) is that every version's wrapper is boilerplate save one
// version string.  Codegen keeps the set consistent and makes "what
// does the version wrapper look like?" a one-place question.
//
// There's also one cross-version file:
//
//   generated/current.ts              re-exports the version named
//                                     by vendor/manifest.json's
//                                     `current` field.  This is what
//                                     a bare `import '<package-name>'`
//                                     resolves to by default (where
//                                     <package-name> is taken from
//                                     package.json — see PACKAGE_JSON).
//
// Usage:
//   bun scripts/emit-version-modules.ts version <version-string> <template>  > <index-out>
//       Emit one version's index.ts from the given template to stdout.
//
//   bun scripts/emit-version-modules.ts current <manifest-json>  > <out>
//       Emit the cross-version re-export shim from the manifest's
//       `current` field to stdout.
//
// The Makefile invokes these per-version; scripts/vendor.ts invokes
// `current` after onboarding a new version.

import { readFileSync } from "node:fs"

import { PACKAGE_JSON, rootRelativePath, runScript } from "./utils.ts"

const SELF = rootRelativePath(__filename)

interface Manifest {
  manifestVersion: number
  current: string
  versions: Record<string, unknown>
}

// ---------------------------------------------------------------------------
// Templates.
// ---------------------------------------------------------------------------

/**
 * One module per version.
 */
function indexModule(version: string, templateContent: string): string {
  return templateContent.replaceAll("__VERSION__", version).replaceAll("__GENERATOR__", SELF)
}

/**
 * Cross-version re-export shim.  A bare `import '<package-name>'`
 * resolves to this file (see package.json's `main`), which re-exports
 * whatever version is marked current in the manifest.  Regenerated
 * whenever the vendor flow lands a new version.
 */
function currentModule(current: string): string {
  return `// AUTO-GENERATED by ${SELF} — do not edit.
//
// Re-export the parser for the lastest SQLite version.
// To depend on a specific version, import from the versioned subpath
// instead: \`${PACKAGE_JSON.name}/sqlite-<version>\`.

export * from "./${current}/index.ts"
`
}

// ---------------------------------------------------------------------------
// CLI.
// ---------------------------------------------------------------------------

const USAGE =
  "usage: scripts/emit-version-modules.ts version <version-string> <template>  > <index-out>\n" +
  "       scripts/emit-version-modules.ts current <manifest-json>  > <out>"

await runScript(import.meta.main, { usage: USAGE }, ({ positionals }) => {
  const [mode, ...rest] = positionals

  if (mode === "version") {
    const [version, templatePath] = rest
    if (!version || !templatePath) {
      console.error(USAGE)
      process.exit(2)
    }
    const templateContent = readFileSync(templatePath, "utf8")
    process.stdout.write(indexModule(version, templateContent))
    return
  }

  if (mode === "current") {
    const [manifestPath] = rest
    if (!manifestPath) {
      console.error(USAGE)
      process.exit(2)
    }
    const manifest = JSON.parse(readFileSync(manifestPath, "utf8")) as Manifest
    process.stdout.write(currentModule(manifest.current))
    return
  }

  console.error(USAGE)
  process.exit(2)
})
